def set_rtl(paragraph):
    """מאלץ פסקה להתיישר לימין ולעבוד בפורמט RTL עברי תקני"""
    paragraph.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    p_pr = paragraph._p.get_or_add_pPr()
    bdo = OxmlElement('w:bdo')
    bdo.set(qn('w:dir'), 'rtl')
    p_pr.append(bdo)

def set_cell_rtl(cell):
    """מאלץ תא בטבלה להתיישר לימין (RTL)"""
    tc_pr = cell._tc.get_or_add_tcPr()
    bdo = OxmlElement('w:bdo')
    bdo.set(qn('w:dir'), 'rtl')
    tc_pr.append(bdo)

